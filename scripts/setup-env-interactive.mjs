#!/usr/bin/env node
/**
 * Guided setup: paste Supabase URL + anon key → writes .env.local
 * Run: npm run setup:env
 */
import * as readline from 'node:readline/promises'
import { stdin as input, stdout as output } from 'node:process'
import { writeFileSync, existsSync, readFileSync } from 'node:fs'
import { resolve } from 'node:path'
import { execSync } from 'node:child_process'
import { platform } from 'node:os'

const rl = readline.createInterface({ input, output })

const dash = 'https://supabase.com/dashboard/projects'
console.log(`
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  Connect this app to your Supabase project
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

  You need TWO copies from the Supabase website:

  ①  Project URL     → looks like  https://xxxxx.supabase.co
  ②  anon public key → long text starting with  eyJ

  On the website:
    • Open your project
    • Click the gear ⚙️  (Project Settings) at the bottom of the left sidebar

    For ① Project URL:
    • Open  "General"  in settings, OR click  "Connect"  at the top of the dashboard
    • Copy the URL like  https://xxxxx.supabase.co

    For ② the public client key:
    • Open  "API Keys"  in settings (you may also see "Data API" — ignore that for keys)
    • Open the  "Legacy API keys"  tab (or similar)
    • Copy the  anon  key labeled  public  (long text, often starts with eyJ)
    • Do NOT use  service_role  or  secret  keys

  Opening the dashboard in your browser…
`)

try {
  if (platform() === 'darwin') execSync(`open "${dash}"`, { stdio: 'ignore' })
  else if (platform() === 'win32') execSync(`start "" "${dash}"`, { shell: true, stdio: 'ignore' })
  else execSync(`xdg-open "${dash}"`, { stdio: 'ignore' })
} catch {
  console.log(`  (Open this yourself: ${dash})\n`)
}

const url = (await rl.question('\n  Paste ① Project URL here, then Enter: ')).trim()
const key = (await rl.question('  Paste ② anon public key here, then Enter: ')).trim()
await rl.close()

if (!url.startsWith('https://')) {
  console.error(`
  URL must start with https://
  Copy "Project URL" from Supabase → Project Settings → API (or General).
`)
  process.exit(1)
}
if (key.length < 32) {
  console.error(`
  Key looks too short. Copy the full client / anon / public key (often one long line starting with eyJ).

  Supabase dashboard:
    Settings (gear) → API → use the key meant for the browser (anon / public / publishable).
  Do NOT paste service_role or secret keys.
`)
  process.exit(1)
}

const envPath = resolve(process.cwd(), '.env.local')
let extra = ''
if (existsSync(envPath)) {
  const old = readFileSync(envPath, 'utf8')
  const keep = old
    .split('\n')
    .filter(
      (l) =>
        l.trim() &&
        !l.startsWith('VITE_SUPABASE_URL') &&
        !l.startsWith('VITE_SUPABASE_ANON_KEY') &&
        !l.startsWith('# Auto-generated by npm run setup:env'),
    )
    .join('\n')
  if (keep.trim()) extra = `\n${keep.trim()}\n`
}

const content = `# Created by npm run setup:env — edit by hand anytime
VITE_SUPABASE_URL=${url}
VITE_SUPABASE_ANON_KEY=${key}

# Storage bucket (must match bucket in Supabase; same as setup SQL)
VITE_CHINESE_CHARACTERS_1_VIDEO_BUCKET=chinese character 1 _videos
${extra}`

writeFileSync(envPath, content)
console.log(`
  ✓ Saved to  ${envPath}

  Next:
    1. Run:  npm run check:supabase-env
       → You should see a big “OK — Supabase env is set” box.
    2. Stop the dev server if it is running  (Ctrl+C), then:  npm run dev
       → You should see:  [tiktokchinese] Supabase env OK — …

`)
